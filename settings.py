# LAMBDA = POPULATION_SIZE (классический вариант)
# - Когда используется: Хороший баланс между исследованием и сохранением.

# LAMBDA < POPULATION_SIZE (тоже допустимо)
# Когда используется:
# - Оценка фитнес-функции очень дорогая (например, требует сложного моделирования), и мы не можем позволить себе много вычислений.
