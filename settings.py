# (Вероятность скрещивания)
# ════════════════════════════════════════════════════════════════
# • 0.6 - 0.9  →  Стандартный диапазон
# • 0.0 - 0.5  →  Слишком мало (почти нет обмена генами)
# • 0.95 - 1.0 →  Слишком много (теряются хорошие комбинации)

